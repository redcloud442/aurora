import NavBar from "@/components/ui/navBar";
import AppSidebar from "@/components/ui/side-bar";
import { SidebarProvider, SidebarTrigger } from "@/components/ui/sidebar";
import { protectionMemberUser } from "@/utils/serversideProtection";
import type { Metadata } from "next";
import { redirect } from "next/navigation";
import "../globals.css";

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function AppLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const {
    profile,
    redirect: redirectTo,
    teamMemberProfile,
  } = await protectionMemberUser();

  if (redirectTo) {
    redirect(redirectTo);
  }
  if (!profile) redirect("/500");

  return (
    <SidebarProvider>
      <div className="flex min-h-screen h-full w-full bg-gray-100">
        {teamMemberProfile.alliance_member_role !== "MEMBER" && (
          <AppSidebar
            userData={profile}
            teamMemberProfile={teamMemberProfile}
          />
        )}

        {/* Main Content Area */}
        <div className="flex-1 flex flex-col overflow-x-hidden">
          {teamMemberProfile.alliance_member_role !== "MEMBER" && (
            <div className="p-4 md:hidden">
              <SidebarTrigger />
            </div>
          )}

          {/* Navbar for MEMBER role */}
          {teamMemberProfile.alliance_member_role === "MEMBER" && (
            <NavBar userData={profile} />
          )}

          {/* Main content */}
          {children}
        </div>
      </div>
    </SidebarProvider>
  );
}
